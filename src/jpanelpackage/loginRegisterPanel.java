    private void loginButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButActionPerformed
        String username = loginUsernameField.getText();
        String password = loginPasswordField.getText();
        if (loginUsernameField.getText().isEmpty() || loginUsernameField.getText().equals("example@email.com or username") || loginPasswordField.getPassword().equals("") || loginPasswordField.getPassword().equals("password")) {
            JOptionPane.showMessageDialog(this, "Username or Password Blank");
        } else {
            if (adminAccountManager.login(username, password) == true) {
                this.hide();
                MainDashboard mainDashboard = new MainDashboard();
                mainDashboard.setVisible(true);
            } else if (adminAccountManager.login(username, password) == false) {
                loginPasswordField.setText("");
                loginUsernameField.requestFocus();
                JOptionPane.showMessageDialog(this, "Username, Email or Password is incorrect");
            }
        }
    }//GEN-LAST:event_loginButActionPerformed

