    private void registerButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButActionPerformed
        // TODO add your handling code here:

        //If Else Statement
        matcher = VALID_EMAIL_ADDRESS_REGEX.matcher(registerEmailField.getText());
        if (!matcher.find() || registerEmailField.getText().equals("example@email.com")) {
            JOptionPane.showMessageDialog(this, "Enter your email. 'example@email.com'");
        } else if (registerFirstNameField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter First Name");

        } else if (registerLastNameTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter Last Name");

        } else if (registerUsernameField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter UserName");

        } else if (registerPhoneNumberTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter Phone number");

        } else if (registerNationalityTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter Nationality");

        } else if (registerIdentityCardOptionComboBox.getSelectedItem().toString().equals("Select Identity Card...")) {
            JOptionPane.showMessageDialog(this, "Select what type of Identity Card is use");

        } else if (registerIdentityIDTextField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter your 'National ID' or 'Passport ID'");

        } else if (adminAccountManager.checkNationalID(registerIdentityCardOptionComboBox.getSelectedItem().toString(), registerIdentityIDTextField.getText())) {
            JOptionPane.showMessageDialog(this, "This indentity card and ID is already used");

        } else if (registerPasswordField.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter Password");

        } else if (registerAddress.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Enter your address");

        } else if (!adminAccountManager.isValidPass(registerPasswordField.getText())) {
            JOptionPane.showMessageDialog(this, "Password must be at least 8 character including digits and chars");

        } else if (registerPasswordField.getText().equals(confirmPassField.getText()) == false) {
            JOptionPane.showMessageDialog(this, "Password Not Matched");

        } else if (adminAccountManager.checkUsername(registerUsernameField.getText())) {
            JOptionPane.showMessageDialog(null, registerUsernameField.getText() + " This username already exit");

        } else if (adminAccountManager.checkEmail(registerEmailField.getText())) {
            JOptionPane.showMessageDialog(this, registerEmailField.getText() + " already exist");

        } else if (registerSecurityQuestionComboBox.getSelectedItem().equals("Select Question...")) {
            JOptionPane.showMessageDialog(this, "Please select any question and answer");

        } else if (registerAnswerField.getText().length() == 0 || registerAnswerField.getText().equals("answer")) {
            JOptionPane.showMessageDialog(this, "Please answer the question..");

        } else {
            adminAccountManager.register(registerEmailField, registerFirstNameField, registerLastNameTextField,
                    registerUsernameField, registerPhoneNumberTextField, registerNationalityTextField,
                    registerIdentityCardOptionComboBox, registerIdentityIDTextField, confirmPassField,
                    registerAddress, registerSecurityQuestionComboBox, registerAnswerField);

            registerEmailField.setText("");
            registerFirstNameField.setText("");
            registerLastNameTextField.setText("");
            registerUsernameField.setText("");
            registerPhoneNumberTextField.setText("");
            registerNationalityTextField.setText("");
            registerIdentityCardOptionComboBox.setSelectedIndex(0);
            registerIdentityIDTextField.setText("");
            registerPasswordField.setText("");
            confirmPassField.setText("");
            registerAddress.setText("");
            registerSecurityQuestionComboBox.setSelectedIndex(0);
            registerAnswerField.setText("");

        }
    }//GEN-LAST:event_registerButActionPerformed

