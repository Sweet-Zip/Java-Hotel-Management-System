    private void saveButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButActionPerformed
        // TODO add your handling code here:
        if (securityAnswerField.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Answer the question");

        } else if (passField.getText().equals("") || passField.getText().equals("password")) {
            JOptionPane.showMessageDialog(null, "Enter the new password");

        } else if (!adminAccountManager.isValidPass(passField.getText())) {
            JOptionPane.showMessageDialog(this, "Password must be at least 8 character including digits and chars");

        } else if (passField.getText().equals(confirmPassField.getText()) == false) {
            JOptionPane.showMessageDialog(this, "The password is not matched!");

        } else {
            adminAccountManager.changePassword(emailField, securityQuestionField, securityAnswerField, passField);
            securityAnswerField.setText("");
            passField.setText("");
            emailField.setText("");
            securityQuestionField.setText("");
            confirmPassField.setText("");
        }
    }//GEN-LAST:event_saveButActionPerformed

