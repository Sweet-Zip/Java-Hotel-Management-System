    private void searchButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButActionPerformed
        // TODO add your handling code here:
        String email, securityQuestion, securityAnswer, password;
        //adminAccountManager.checkValidEmail(emailField, securityQuestionField, securityAnswerField, emailField);
//        adminAccount.setEmail(emailField.getText());
//        adminAccount.setSecurityQuestion(securityQuestionField.getText());
//        adminAccount.setSecurityAnswer(securityAnswerField.getText());
//        adminAccount.setPassword(passField.getText());
        int check = 0;
        query = "SELECT * FROM `user_login` WHERE `email` =?";
        emailField.getText();
        if (emailField.getText().equals(0) || emailField.getText().equals("example@email.com")) {
            check = 1;
            JOptionPane.showMessageDialog(null, "Please Enter your Email");
        } else {
            try {
                con = connectionProvider.getCon();
                pst = con.prepareStatement(query);
                pst.setString(1, emailField.getText());
                resultSet = pst.executeQuery();

                if (resultSet.next()) {
                    check = 1;
                    securityAnswerField.setEditable(true);
                    securityQuestionField.setEditable(false);
                    emailField.setEditable(false);
                    securityQuestionField.setText(resultSet.getString(12));
                    resultSet.close();
                    pst.close();
                }

            } catch (SQLException ex) {
                Logger.getLogger(forgetPasswordJPanel.class.getName()).log(Level.SEVERE, null, ex);
            }

        }
        if (check == 0) {
            JOptionPane.showMessageDialog(null, "Incorrect Email");
        }

    }//GEN-LAST:event_searchButActionPerformed

